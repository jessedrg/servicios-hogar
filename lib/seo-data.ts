// Helper function to get a profession by slug
export function getProfessionBySlug(slug: string): Profession | undefined {
  return PROFESSIONS.find((p) => p.slug === slug)
}

// Helper function to get a city by slug
export function getCityBySlug(slug: string): City | undefined {
  return CITIES_SPAIN.find((c) => c.slug === slug)
}

// Helper function to get cities by region
export function getCitiesByRegion(region: string): City[] {
  return CITIES_SPAIN.filter((c) => c.region === region)
}

// Helper function to get cities by province
export function getCitiesByProvince(province: string): City[] {
  return CITIES_SPAIN.filter((c) => c.province === province)
}

// Helper function to get a problem by slug within a profession
export function getProblemBySlug(professionSlug: string, problemSlug: string): Problem | undefined {
  const profession = getProfessionBySlug(professionSlug)
  if (!profession) return undefined
  return profession.problems.find((p) => p.slug === problemSlug)
}

